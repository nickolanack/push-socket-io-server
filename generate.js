const readline = require('readline');

const rl = readline.createInterface({
	input: process.stdin,
	output: process.stdout
});

const run=()=>{

	adjectives.sort(()=>{return Math.random()-0.5});
	nouns.sort(()=>{return Math.random()-0.5});

	

	const question=(question)=>{

		return new Promise((resolve)=>{
			rl.question(question, (answer) => {
				resolve(answer);
			});
		});

	}

	let projectName=adjectives.shift()+'-'+nouns.shift();
	let appID=""

	var crypto=require('crypto');
	var shasum = crypto.createHash('md5');
	shasum.update((new Date()).getTime()+"");
	appID=shasum.digest('hex');

	let username="";

	shasum = crypto.createHash('sha1');
	shasum.update(((new Date()).getTime()*Math.random())+"");
	username=shasum.digest('base64');


	

	let password="";

	shasum = crypto.createHash('sha1');
	shasum.update(((new Date()).getTime()*Math.random())+"");
	password=shasum.digest('base64');



	question('Project Name?:['+projectName+'] ')
	.then((answer)=>{

		projectName=answer.trim()?answer.trim():projectName;
		console.log('>> '+projectName);

		return question('Username?:['+username+'] ');
		
	}).then((answer)=>{

		username=answer.trim()?answer.trim():username;
		console.log('>> '+username);


		return question('Password?:['+password+'] ');

	}).then((answer)=>{

		password=answer.trim()?answer.trim():password;
		console.log('>> '+password);

		console.log('echo \''+JSON.stringify({
				"appid":appID,
				"project":projectName,
				"username":username,
				"password":password
			})+'\' > appdata/'+appID+'.json');
		
		rl.close();

		


	}).catch((e)=>{


		console.error(e);

		rl.close();

	});


	
}

